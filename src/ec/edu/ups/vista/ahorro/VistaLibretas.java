    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        boolean bander = true;
        if (0 == codigoPersona) {
            bander = false;
        }
        if (0 >= txtDescripcionB.getText().length()) {
            bander = false;
        }
        if (0 >= txtTotal.getText().length()) {
            bander = false;
        }
        if (bander) {
            Libretas libretas = controlador.findByID(Integer.valueOf(txtNumeroLibreta.getText()));
            ControladorPersona controladorPersona = new ControladorPersona();
            Persona persona = controladorPersona.findByID(codigoPersona);
            libretas.setIdUsuario(persona);
            libretas.setTotal(BigDecimal.valueOf(Double.parseDouble(txtTotal.getText())));
            if (check.isSelected()) {
                libretas.setEstado(new Short("1"));
            } else {
                libretas.setEstado(new Short("0"));
            }
            libretas.setDescripcion(txtDescripcionB.getText());
            controlador.edit(libretas);
            JOptionPane.showMessageDialog(this, "Libreta Actualizada Correctamete");
            txtCedulaB.setText("");
            txtClienteB.setText("");
            txtTelefonoB.setText("");
            txtDirecionB.setText("");
            txtDescripcionB.setText("");
            txtTotal.setText("");
            check.setText("");
            check.setSelected(false);
        }else{
            JOptionPane.showMessageDialog(this, "Error al actualizar");
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

