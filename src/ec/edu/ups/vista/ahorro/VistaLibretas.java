    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean bander = true;
        if (0 == codigoPersona) {
            bander = false;
        }
        if (0 >= txtDescripcion.getText().length()) {
            bander = false;
        }

        if (bander) {
            ControladorPersona controladorPersona = new ControladorPersona();
            Persona titulaCuenta = controladorPersona.findByID(codigoPersona);

            Libretas libreta = new Libretas(0, new Short("1"), txtDescripcion.getText(), BigDecimal.valueOf(Double.parseDouble("0")), titulaCuenta);
            try {
                if (titulaCuenta != null) {
                    controlador.Crear(libreta);
                    txtCedula.setText("");
                    txtCliente.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    txtDescripcion.setText("");

                    JOptionPane.showMessageDialog(this, "CUENTA CREADA");
                } else {
                    JOptionPane.showMessageDialog(this, "NO SE ECUENTRA AL TITULAR");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Exiten errores en los campos!! \nVerfique los datos ingresados", "USUARIO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

