    public void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaLibretas.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    public void llenarTabla() {
        ControladorLibreta controladorLibreta = new ControladorLibreta();
        List<Libretas> listaLibrets = controladorLibreta.findAll();

        DefaultTableModel model = (DefaultTableModel) tablaLibretas.getModel();
        
        int index = 0;
        for (Libretas u : listaLibrets) {
            String estado = String.valueOf(u.getEstado());
            if (estado.equals("1")) {
                estado = "ACTIVA";
            } else {
                check.setSelected(false);
                estado = "INACTIVA";
            }
            model.insertRow(index, new Object[]{u.getIdLibretas(), u.getIdUsuario().getNombre() + " " + u.getIdUsuario().getApellido(), estado, u.getDescripcion(), u.getTotal()});
            index++;
        }

    }

