    public void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaLibretas.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }


    private void buscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar2ActionPerformed
        ControladorLibreta controladorLibreta = new ControladorLibreta();
        Libretas libreta = controladorLibreta.findByID(Integer.valueOf(txtNumeroLibretaB.getText()));

        DefaultTableModel model = (DefaultTableModel) tablaLibretas.getModel();
        int index = 0;

        if (libreta != null) {
            List<Ahorro> listaAhorros = controlador.findAll();
            for (Ahorro ahorro : listaAhorros) {
                if (ahorro.getIdLibretas().equals(libreta)) {
                    model.insertRow(index, new Object[]{ahorro.getIdAhorro(),ahorro.getFecha(),ahorro.getCantidad()});
                    index++;
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se ecuentra la libreta");
        }
    }//GEN-LAST:event_buscar2ActionPerformed


