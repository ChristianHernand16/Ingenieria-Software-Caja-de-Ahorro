    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        boolean bander = true;

        if (0 >= txtMonto.getText().length()) {
            bander = false;
        }
        if (null == libretasAux) {
            bander = false;
        }
        if (bander) {
            Date date = new Date();
            Ahorro ahorro = new Ahorro(0, date, BigDecimal.valueOf(Double.parseDouble(txtMonto.getText())), libretasAux);
            controlador.Crear(ahorro);
            ControladorLibreta controladorLibreta = new ControladorLibreta();
            BigDecimal total = BigDecimal.valueOf(Double.parseDouble(txtMonto.getText()) + Double.parseDouble(libretasAux.getTotal().toString()));
            libretasAux.setTotal(total);
            controladorLibreta.edit(libretasAux);
            libretasAux = null;
            JOptionPane.showMessageDialog(this, "Deposito Realizado");
            txtCedulaB.setText("");
            txtClienteB.setText("");
            txtTelefonoB.setText("");
            txtDirecionB.setText("");
            txtDescripcionB.setText("");
            check.setSelected(false);
            check.setText("");
            txtTotal.setText("");
            txtMonto.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar deposito verifique datos");
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

