    private void buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar1ActionPerformed
        ControladorLibreta controladorLibreta = new ControladorLibreta();
        Libretas libreta = controladorLibreta.findByID(Integer.valueOf(txtNumeroLibreta.getText()));
        if (libreta != null) {
            libretasAux = libreta;
            String estado = String.valueOf(libreta.getEstado());
            if (estado.equals("1")) {
                check.setSelected(true);
                check.setText("ACTIVA");
                txtCedulaB.setText(libreta.getIdUsuario().getCedula());
                txtClienteB.setText(libreta.getIdUsuario().getNombre() + " " + libreta.getIdUsuario().getApellido());
                txtTelefonoB.setText(libreta.getIdUsuario().getTelefono());
                txtDirecionB.setText(libreta.getIdUsuario().getDireccion());
                txtDescripcionB.setText(libreta.getDescripcion());
                txtTotal.setText(libreta.getTotal() + "");
            } else {
                JOptionPane.showMessageDialog(this, "Libreta no esta activa");
                txtClienteB.setText("");
                txtCedulaB.setText("");
                txtTelefonoB.setText("");
                txtDirecionB.setText("");
                txtDescripcionB.setText("");
                check.setSelected(false);
                check.setText("");
                txtTotal.setText("");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Libreta no econtrada");
            txtClienteB.setText("");
            txtCedulaB.setText("");
            txtTelefonoB.setText("");
            txtDirecionB.setText("");
            txtDescripcionB.setText("");
            check.setSelected(false);
            check.setText("");
            txtTotal.setText("");
            txtMonto.setText("");
        }
    }//GEN-LAST:event_buscar1ActionPerformed

